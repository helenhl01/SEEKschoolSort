    public void setWednesday1(int wednesday1){ this.wednesday1 = wednesday1;}
    public void setWednesday2(int wednesday2){ this.wednesday2 = wednesday2;}
    public void setThursday1(int thursday1){ this.thursday1 = thursday1;}
